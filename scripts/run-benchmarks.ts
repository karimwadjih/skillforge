import { spawn } from "node:child_process";
import { readdir, readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { join } from "node:path";

type BenchmarkCase = {
  slug: string;
  maturity: string;
  prompts: Array<{
    id: string;
    title: string;
    prompt: string;
    minimum_expectations: string[];
    rubric_dimensions: string[];
  }>;
};

type BenchmarkResult = {
  slug: string;
  adapter: string;
  executed_at: string;
  prompts: Array<{
    id: string;
    title: string;
    score: number;
    matched_expectations: number;
    total_expectations: number;
    notes: string;
    output_excerpt?: string;
  }>;
  aggregate_score: number;
  note: string;
};

type CommandAdapterResponse = {
  output?: string;
  score?: number;
  matched_expectations?: number;
  notes?: string;
};

function mockScore(caseFile: BenchmarkCase): BenchmarkResult {
  const prompts = caseFile.prompts.map((prompt) => {
    const matched = prompt.minimum_expectations.length;
    const score = Number((0.7 + Math.min(0.28, matched * 0.09)).toFixed(2));
    return {
      id: prompt.id,
      title: prompt.title,
      score,
      matched_expectations: matched,
      total_expectations: prompt.minimum_expectations.length,
      notes: "Mock adapter: counts declared expectations to demonstrate result loading and scorecard structure."
    };
  });

  const aggregate = Number(
    (prompts.reduce((sum, prompt) => sum + prompt.score, 0) / prompts.length).toFixed(2)
  );

  return {
    slug: caseFile.slug,
    adapter: "mock",
    executed_at: new Date().toISOString(),
    prompts,
    aggregate_score: aggregate,
    note: "This is a deterministic example result generated by the mock adapter. It is useful for tooling demos, not for claiming real model performance."
  };
}

async function runCommandAdapter(command: string, payload: Record<string, unknown>) {
  return new Promise<CommandAdapterResponse>((resolve, reject) => {
    const child = spawn(command, {
      shell: true,
      stdio: ["pipe", "pipe", "pipe"]
    });

    let stdout = "";
    let stderr = "";

    child.stdout.on("data", (chunk) => {
      stdout += chunk.toString();
    });

    child.stderr.on("data", (chunk) => {
      stderr += chunk.toString();
    });

    child.on("error", reject);
    child.on("close", (code) => {
      if (code !== 0) {
        reject(new Error(`Command adapter exited with code ${code}: ${stderr.trim() || "no stderr"}`));
        return;
      }

      try {
        resolve(JSON.parse(stdout) as CommandAdapterResponse);
      } catch (error) {
        reject(new Error(`Command adapter did not return valid JSON. ${String(error)}\n${stdout}`));
      }
    });

    child.stdin.write(JSON.stringify(payload));
    child.stdin.end();
  });
}

async function commandScore(caseFile: BenchmarkCase, command: string): Promise<BenchmarkResult> {
  const prompts = [];

  for (const prompt of caseFile.prompts) {
    const response = await runCommandAdapter(command, {
      slug: caseFile.slug,
      prompt
    });
    const totalExpectations = prompt.minimum_expectations.length;
    const matched =
      response.matched_expectations ??
      (typeof response.score === "number"
        ? Math.max(0, Math.min(totalExpectations, Math.round(response.score * totalExpectations)))
        : 0);
    const score =
      typeof response.score === "number"
        ? Number(response.score.toFixed(2))
        : totalExpectations === 0
          ? 0
          : Number((matched / totalExpectations).toFixed(2));

    prompts.push({
      id: prompt.id,
      title: prompt.title,
      score,
      matched_expectations: matched,
      total_expectations: totalExpectations,
      notes:
        response.notes ??
        "Command adapter result. Scoring came from the adapter response or was derived from matched expectations when available.",
      output_excerpt: response.output?.slice(0, 240)
    });
  }

  const aggregate = Number(
    (prompts.reduce((sum, prompt) => sum + prompt.score, 0) / prompts.length).toFixed(2)
  );

  return {
    slug: caseFile.slug,
    adapter: "command",
    executed_at: new Date().toISOString(),
    prompts,
    aggregate_score: aggregate,
    note: "These results were generated through the optional command adapter. Skillforge does not claim cross-team comparability or provider neutrality beyond the result schema itself."
  };
}

async function main() {
  const root = fileURLToPath(new URL("..", import.meta.url));
  const caseDir = join(root, "evals", "benchmark-cases");
  const resultsDir = join(root, "evals", "results");
  const files = (await readdir(caseDir)).filter((file) => file.endsWith(".json"));
  const results: BenchmarkResult[] = [];
  const adapter = process.env.SKILLFORGE_BENCHMARK_ADAPTER ?? "mock";
  const command = process.env.SKILLFORGE_BENCHMARK_COMMAND;

  for (const file of files) {
    const parsed = JSON.parse(await readFile(join(caseDir, file), "utf8")) as BenchmarkCase;
    if (adapter === "command") {
      if (!command) {
        throw new Error("SKILLFORGE_BENCHMARK_COMMAND must be set when SKILLFORGE_BENCHMARK_ADAPTER=command.");
      }
      results.push(await commandScore(parsed, command));
    } else {
      results.push(mockScore(parsed));
    }
  }

  const outputPath = join(resultsDir, `generated-${adapter}-results.json`);
  await writeFile(outputPath, `${JSON.stringify(results, null, 2)}\n`, "utf8");
  console.log(JSON.stringify({ adapter, cases: results.length, output: outputPath }, null, 2));
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
